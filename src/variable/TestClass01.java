package variable;

public class TestClass01 {
	public static void main(String[] args) {
	/*
	 * 변수
	 * 데이터를 저장하는 공간
	 * 
	 * 자료형
	 * int: 정수	 		4byte(32bit)
	 * char: 문자하나	 	2byte
	 * float: 실수		4byte
	 * double: 실수		8byte
	 * boolean: true	1byte
	 * 
	 * 생성규칙
	 * 첫 문자는 소문자, 합성어의 첫 문자는 대문자 (ex. youName)
	 * 변수이름에 직관적인 의미 부여
	 * */
		
	int age = 20;
	double weight = 55.2;
	
	System.out.println("나의 나이는 " + age + "살 입니다.");
	System.out.println("나의 몸무게 " + weight );
	
	
	}
}
